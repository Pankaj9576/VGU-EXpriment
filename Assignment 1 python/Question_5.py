''' Question 5
Explain the difference between an expression and a statement, using examples if necessary.

Ans:-Expression:= An expression is a combination of values, variables, operators, and function calls that evaluates to a single value. 
It evaluates to a single value and can be part of a statement or stand alone.

Statement:= A statement is a complete line of code that performs an action. It can include expressions but doesn't necessarily
 return a value. It performs an action and can include expressions but doesn't always return a value.

'''
# Example
